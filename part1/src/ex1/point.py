'''
 Class to represent point
'''
class Point:
  def __init__(self, x, y):
    self.x = x
    self.y = y

  def print_point(self):
    print("Point(%s,%s)"%(self.x, self.y)) 
    return

  def get_x():
    return self.x

  def get_y():
    return self.y

  def get_point():
    return (self.x, self.y)